"""`esp-harness new <name>` — scaffold a fresh ESP-IDF + LVGL project.

Three vendoring modes for aurora-harness:

  --component-source vendor   (default)  copy the whole component into
                                          the new project's components/.
                                          Project is self-contained.
  --component-source link                 EXTRA_COMPONENT_DIRS points at
                                          the monorepo's components/.
                                          Lighter footprint; depends on
                                          the monorepo staying put.
  --component-source depend               write idf_component.yml dep
                                          (caldis/aurora-harness). Needs
                                          the component to be on the
                                          Registry (TBD).

`init` is kept as a back-compat alias for the same command.

Templates live as Python string constants in this file — they're tiny
and changing them shouldn't require a separate template directory.
"""

