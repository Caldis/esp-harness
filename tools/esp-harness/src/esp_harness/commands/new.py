def add_subparser(sub, add_common_flags) -> None:
    p = sub.add_parser(
        "new",
        help="Scaffold a fresh ESP-IDF + LVGL project consuming aurora-harness.",
        description=(
            "Creates a directory <name>/ with a minimal app_main, a 'Hello' "
            "scene, and CMakeLists wiring aurora-harness via one of three "
            "vendoring strategies (--component-source vendor|link|depend)."
        ),
    )
    p.add_argument("name", help="Project directory to create. Must not exist yet.")
    p.add_argument(
        "--component-source", "--source",
        choices=("vendor", "link", "depend"),
        default="vendor",
        help=(
            "How the new project consumes aurora-harness: "
            "vendor (default) copies the component into the project; "
            "link points EXTRA_COMPONENT_DIRS at the monorepo; "
            "depend writes an idf_component.yml dependency (requires "
            "the component to be published to the Component Registry)."
        ),
    )
    p.add_argument(
        "--harness-root", type=Path, default=None,
        help="Override the monorepo root (where components/aurora-harness/ lives). "
             "Default: auto-detect from the toolkit's install location.",
    )
    add_common_flags(p)


def _c_safe_name(name: str) -> str:
    """Make a project name safe for C identifiers: replace -+./ with _."""
    return "".join(c if c.isalnum() else "_" for c in name)


def _detect_harness_root(override: Path | None) -> Path | None:
    """Walk up from this file to find the monorepo root."""
    if override:
        return override if (override / "components" / "aurora-harness").exists() else None
    toolkit_root  = Path(__file__).resolve().parents[3]
    monorepo_root = toolkit_root.parents[1]
    if (monorepo_root / "components" / "aurora-harness").exists():
        return monorepo_root
    return None


def _write(out: Path, content: str, output: Output) -> None:
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(content, encoding="utf-8", newline="\n")
    output.debug(f"wrote {out}")


def run(args: argparse.Namespace, output: Output) -> int:
    target = Path(args.name).resolve()
    if target.exists() and any(target.iterdir()):
        output.failure(
            exit_code=GENERIC_ERROR,
            error=f"refusing to scaffold into non-empty directory {target}",
        )
        return GENERIC_ERROR

    cname = _c_safe_name(target.name)
    source = args.component_source

    harness_root = _detect_harness_root(args.harness_root)
    if source in ("vendor", "link") and not harness_root:
        output.failure(
            exit_code=GENERIC_ERROR,
            error=(
                "Cannot find esp-harness monorepo (no components/aurora-harness/ "
                "in expected location). Pass --harness-root /path/to/esp-harness, "
                "or use --component-source depend (registry, requires publish)."
            ),
        )
        return GENERIC_ERROR

    # Plan the CMake fragments + README note per source mode.
    if source == "vendor":
        source_note = (
            "#   vendor: components/aurora-harness/ is copied into ./components/.\n"
            "#           This project is self-contained — no external deps.\n"
            "#           Re-vendor by re-running `esp-harness new` against a newer\n"
            "#           harness clone, or copy in updates manually."
        )
        extra_dirs = (
            "# components/ already contains aurora-harness (vendored). "
            "ESP-IDF auto-discovers components in ./components/, so no\n"
            "# EXTRA_COMPONENT_DIRS needed.\n"
            "# If you add a board BSP from the esp-harness monorepo, append:\n"
            "#   list(APPEND EXTRA_COMPONENT_DIRS /path/to/esp-harness/boards)"
        )
        vendor_note = (
            "This project **vendored** aurora-harness into `components/aurora-harness/`. "
            "It's self-contained — no external clone needed. To update, replace the "
            "vendored copy with a newer one from "
            "[esp-harness](https://github.com/Caldis/esp-harness)."
        )
    elif source == "link":
        source_note = (
            f"#   link: EXTRA_COMPONENT_DIRS points at the monorepo at\n"
            f"#         {harness_root}. Lighter footprint than vendor; depends\n"
            f"#         on that path remaining valid."
        )
        extra_dirs = (
            f"if(NOT DEFINED ESP_HARNESS_ROOT)\n"
            f"    set(ESP_HARNESS_ROOT \"{harness_root.as_posix()}\")\n"
            f"endif()\n"
            f"list(APPEND EXTRA_COMPONENT_DIRS\n"
            f"    ${{ESP_HARNESS_ROOT}}/components\n"
            f"    ${{ESP_HARNESS_ROOT}}/boards\n"
            f")"
        )
        vendor_note = (
            f"This project **links** aurora-harness from `{harness_root}`. "
            "If you move the esp-harness clone, update `ESP_HARNESS_ROOT` "
            "in the top-level CMakeLists.txt accordingly."
        )
    else:  # depend
        source_note = (
            "#   depend: aurora-harness pulled via idf_component.yml from the\n"
            "#           ESP-IDF Component Registry. Requires `caldis/aurora-harness`\n"
            "#           to be published (status: not yet, as of v1.5)."
        )
        extra_dirs = (
            "# aurora-harness fetched via the component manager — see\n"
            "# main/idf_component.yml. No EXTRA_COMPONENT_DIRS needed."
        )
        vendor_note = (
            "This project **depends** on aurora-harness via the ESP-IDF "
            "Component Registry. `idf.py reconfigure` will fetch it into "
            "`managed_components/`. Pin the version in `main/idf_component.yml`."
        )

    top_cmake = (TOP_CMAKE
                 .replace("__NAME__", cname)
                 .replace("__SOURCE_NOTE__", source_note)
                 .replace("__EXTRA_COMPONENT_DIRS__", extra_dirs))

    files: dict[str, str] = {
        "CMakeLists.txt":                       top_cmake,
        "main/CMakeLists.txt":                  MAIN_CMAKE.replace("__NAME__", cname),
        f"main/{cname}_main.c":                 MAIN_C.replace("__NAME__", cname),
        "main/scenes/scene_hello.c":            SCENE_HELLO_C.replace("__NAME__", cname),
        "main/scenes/scenes.h":                 SCENES_H,
        "README.md":                            (README_MD
                                                  .replace("__NAME__", cname)
                                                  .replace("__VENDOR_NOTE__", vendor_note)),
        "AGENT.md":                             AGENT_MD.replace("__NAME__", cname),
        "sdkconfig.defaults":                   SDKCONFIG_DEFAULTS,
        "partitions.csv":                       PARTITIONS_CSV,
        ".gitignore":                           GITIGNORE,
    }
    if source == "depend":
        # Write an idf_component.yml so the component manager fetches the dep.
        files["main/idf_component.yml"] = (
            "## Dependencies fetched by the IDF Component Manager.\n"
            "## `idf.py reconfigure` populates managed_components/.\n"
            "\n"
            "dependencies:\n"
            "  lvgl/lvgl: \"^9.0\"\n"
            "  caldis/aurora-harness: \"^1.5.0\"\n"
            "  ## Add your board's BSP component dep here.\n"
        )

    for relpath, content in files.items():
        _write(target / relpath, content, output)

    # vendor mode: actually copy the component into the new project's components/
    if source == "vendor":
        src_comp = harness_root / "components" / "aurora-harness"
        dst_comp = target / "components" / "aurora-harness"
        try:
            shutil.copytree(src_comp, dst_comp,
                            ignore=shutil.ignore_patterns("build", "managed_components",
                                                          "__pycache__"))
            output.info(f"vendored: {src_comp} -> {dst_comp}")
        except Exception as e:
            output.failure(exit_code=GENERIC_ERROR,
                           error=f"failed to copy aurora-harness into project: {e}")
            return GENERIC_ERROR

    output.success(
        {
            "project": str(target),
            "c_name": cname,
            "component_source": source,
            "harness_root": str(harness_root) if harness_root else None,
            "files": list(files.keys()),
        },
        human=(
            f"scaffolded {cname} at {target} (source: {source})\n"
            f"  cd {target}\n"
            f"  esp-harness build\n"
            f"  esp-harness flash"
        ),
    )
    return OK
