"""`esp-harness init <name>` — generate a starter ESP-IDF + LVGL project.

The minimum viable project that consumes `aurora-harness`:
  - app_main wiring (NVS / display / console / scene framework)
  - one demo scene
  - CMakeLists pointing at a sibling esp32-harness-showcase clone for the
    aurora-harness component
  - README with the quickstart commands

After running `esp-harness init my-thing`, the user should be able to
`cd my-thing && esp-harness build && esp-harness flash` and see a
"Hello" scene on the device. Everything else is iteration.
"""

