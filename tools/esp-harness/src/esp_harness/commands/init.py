README_MD = """\
# __NAME__

ESP-IDF + LVGL project generated by `esp-harness init`. Consumes the
[`aurora-harness`](https://github.com/Caldis/esp32-harness-showcase/tree/master/components/aurora-harness)
component for console protocol + scene framework.

## Quickstart

```bash
# Auto-discover board on a USB port:
esp-harness port detect

# Build + flash + boot:
esp-harness build
esp-harness flash
esp-harness monitor --seconds 8 --until "ready"

# All-in-one:
esp-harness run --until "ready"
```

## Layout

```
__NAME__/
├── CMakeLists.txt              ESP-IDF top-level project file
├── main/
│   ├── CMakeLists.txt          main component registration
│   ├── __NAME___main.c         app_main + display + scene framework wiring
│   └── scenes/
│       ├── scenes.h
│       └── scene_hello.c       minimal scene template
└── sdkconfig.defaults          recommended starting Kconfig
```

## Adding a new scene

1. Copy `main/scenes/scene_hello.c` to `main/scenes/scene_<name>.c`.
2. Define a fresh `scene_t scene_<name> = { ... };` at the bottom.
3. Add `extern scene_t scene_<name>;` to `main/scenes/scenes.h`.
4. List the new `.c` in `main/CMakeLists.txt` SRCS.
5. `scene_fw_register(&scene_<name>);` in `__NAME___main.c`.

That's it — the scene shows up in `esp-harness console --cmd "scene list"`
automatically.

## Adding a console command

```c
static int cmd_hello(const console_args_t *args) {
    (void)args;
    console_reply_ok("hi");
    return 0;
}

static const console_cmd_t s_cmd_hello = { "?hello", cmd_hello, "say hi" };

void register_my_cmds(void) {
    console_protocol_register(&s_cmd_hello);
}
```

Call `register_my_cmds()` from `app_main` after `console_protocol_init()`.

## Where is aurora-harness?

By default this project expects `esp32-harness-showcase` cloned in
a sibling directory:

```
parent/
├── esp32-harness-showcase/     <-- contains components/aurora-harness/
└── __NAME__/                   <-- you are here
```

Override at build time: `cmake -DAURORA_HARNESS_DIR=/abs/path/components`.

## What you got

- **Scene framework** — a deck of full-screen LVGL views with lifecycle
  hooks. Register scenes once, navigate via console (`scene next` /
  `scene <id>`) or touch.
- **Console protocol** — `OK:` / `ERR:` / `EVT:` framing + multi-line
  payload tags. Universal AI-driveable serial interface.
- **`?stat` command** — fps / heap / scene state as JSON.
- **`?ping` / `?help` / `?reset`** — built-ins.

Add more capabilities by registering more commands and scenes. See
[`aurora-harness/README.md`](https://github.com/Caldis/esp32-harness-showcase/blob/master/components/aurora-harness/README.md)
for the full API.
"""

