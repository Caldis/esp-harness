def add_subparser(sub, add_common_flags) -> None:
    p = sub.add_parser(
        "init",
        help="Generate a starter ESP-IDF + LVGL project that uses aurora-harness.",
        description=(
            "Creates a directory <name>/ with a minimal app_main, a 'Hello' "
            "scene, and CMakeLists pointing at a sibling esp32-harness-showcase "
            "clone for the aurora-harness component. Customise from there."
        ),
    )
    p.add_argument("name", help="Project directory to create. Must not exist yet.")
    p.add_argument(
        "--harness-dir", type=Path, default=None,
        help="Override path to the directory holding aurora-harness/. "
             "Default: ../esp32-harness-showcase/components (sibling repo).",
    )
    add_common_flags(p)


def _c_safe_name(name: str) -> str:
    """Make a project name safe for C identifiers: replace -+./ with _."""
    return "".join(c if c.isalnum() else "_" for c in name)


def _write(out: Path, content: str, output: Output) -> None:
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(content, encoding="utf-8", newline="\n")
    output.debug(f"wrote {out}")


def run(args: argparse.Namespace, output: Output) -> int:
    target = Path(args.name).resolve()
    if target.exists() and any(target.iterdir()):
        output.failure(
            exit_code=GENERIC_ERROR,
            error=f"refusing to scaffold into non-empty directory {target}",
        )
        return GENERIC_ERROR

    name = target.name                  # display name (kebab-case ok)
    cname = _c_safe_name(name)          # used in symbols & filenames

    def render(template: str) -> str:
        # Order matters: do the C-safe substitution *first* in case the
        # raw name contains characters that would break C identifiers,
        # then the display name. Today both forms use __NAME__ — pick
        # one per file based on what's safest in context (we use cname
        # everywhere to keep things simple).
        return template.replace("__NAME__", cname)

    files = {
        "CMakeLists.txt":                       render(TOP_CMAKE),
        "main/CMakeLists.txt":                  render(MAIN_CMAKE),
        f"main/{cname}_main.c":                 render(MAIN_C),
        "main/scenes/scene_hello.c":            render(SCENE_HELLO_C),
        "main/scenes/scenes.h":                 SCENES_H,
        "README.md":                            render(README_MD),
        "sdkconfig.defaults":                   SDKCONFIG_DEFAULTS,
        "partitions.csv":                       PARTITIONS_CSV,
        ".gitignore":                           GITIGNORE,
    }

    for relpath, content in files.items():
        _write(target / relpath, content, output)

    output.success(
        {
            "project": str(target),
            "c_name": cname,
            "files": list(files.keys()),
            "harness_hint": (
                str(args.harness_dir) if args.harness_dir
                else "expects ../esp32-harness-showcase/components"
            ),
        },
        human=(
            f"scaffolded {cname} at {target}\n"
            f"  cd {target}\n"
            f"  esp-harness build\n"
            f"  esp-harness flash"
        ),
    )
    return OK
